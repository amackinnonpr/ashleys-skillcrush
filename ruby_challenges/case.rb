weather_now = 'sunny'

case weather_now
when 'cloudy'
	puts 'Might want a sweatshirt!'
when 'rainy'
	puts "Boots and a raincoat are sure to get you thru!"
when 'foggy'
	puts "Sometimes hope gets lost in the fog!"
when 'snowy'
	puts "Grab some mittens and make a snowman!"
when 'icy'
	puts "Pull out the ice skates, it's slippery!"
when 'stormy'
	puts "Grab your coat for the storm!"
when 'windy'
	puts "Hold on to your hats! It's a windy one!"
else
	puts "It's hard to tell what you should wear!"
end
